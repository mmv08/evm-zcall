import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import process from "node:process";

const projectRoot = process.cwd();
const artifactPath = join(projectRoot, "out/ZCall.yul/ZCall.json");
const outputPath = join(projectRoot, "src/sdk/generated/initcode.ts");

const artifact = JSON.parse(await readFile(artifactPath, "utf8"));
const bytecode = artifact?.bytecode?.object;

if (typeof bytecode !== "string" || !/^0x[0-9a-fA-F]+$/.test(bytecode)) {
	throw new TypeError(`Missing or invalid bytecode in ${artifactPath}`);
}

if (bytecode === "0x") {
	throw new RangeError(`Expected non-empty bytecode in ${artifactPath}`);
}

const contents = `// This file is generated by scripts/generate-sdk-initcode.mjs.
// Run npm run build:contracts after changing src/ZCall.yul.

export const zcallInitcode =
\t${JSON.stringify(bytecode)} as const;
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, contents);
